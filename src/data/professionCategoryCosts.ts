function classifySpellCasterArchetype(realms: string[]): SpellCasterArchetype {
  const hasArms = realms.includes("Arms");
  const magicalRealmCount = realms.filter((realm) => realm !== "Arms").length;

  if (magicalRealmCount === 0) return "arms";
  if (hasArms) return "semi";
  if (magicalRealmCount === 1) return "pure";
  return "hybrid";
}

function applySpellCategoryCosts(
  baseCosts: Record<string, Record<string, string>>,
): Record<string, Record<string, string>> {
  const realmByProfession = Object.fromEntries(
    PROFESSIONS_DATA.map((profession) => [profession.name, [...profession.realmOfPower]]),
  ) as Record<string, string[]>;

  const result: Record<string, Record<string, string>> = {};
  for (const [professionName, professionCosts] of Object.entries(baseCosts)) {
    const archetype = classifySpellCasterArchetype(realmByProfession[professionName] ?? ["Arms"]);
    const spellCosts = SPELL_CATEGORY_COSTS_BY_ARCHETYPE[archetype];

    result[professionName] = { ...professionCosts };
    for (const categoryName of SPELL_CATEGORY_NAMES) {
      result[professionName][categoryName] = spellCosts[categoryName] ?? "";
    }
  }

  return result;
}

